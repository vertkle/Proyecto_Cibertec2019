package Modelo;
public class ProfesionPOJO {
    private int idprof;
    private String profesion;

    public ProfesionPOJO(int idprof, String profesion) {
        this.idprof = idprof;
        this.profesion = profesion;
    }

    public int getIdprof() {
        return idprof;
    }

    public String getProfesion() {
        return profesion;
    }
    
    
}
